package BasicJava.Operators;

public class ShiftOperator {
    public static void main(String[] args) {

        int a =10 ;
        System.out.println(">> :" +(a>>1));

        System.out.println("<< :" +(a<<1));

        System.out.println(">>> :" +(a>>>1));

    }
}

/* output
            >> :5
            >> :20
            >> :5

            */
