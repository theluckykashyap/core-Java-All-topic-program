package BasicJava.Operators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a=20;
        int b=3;
        // Addition
        System.out.println("it is Addition Operator (a+b) : "+(a+b));
        //subtraction
        System.out.println("it is subtraction Operator (a-b) : "+(a-b));
        //multiplication
        System.out.println("it is multiplication Operator (a*b) : "+(a*b));
        //Division
        System.out.println("it is Division Operator (a/b) : "+(a/b));
        //Modulo
        System.out.println("it is Modulo Operator (a%b) : "+(a%b));

      /*
        Output:-

       .Operators.ArithmeticOperator
        it is Addition Operator (a+b) : 23
        it is subtraction Operator (a-b) : 17
        it is multiplication Operator (a*b) : 60
        it is Division Operator (a/b) : 6
        it is Modulo Operator (a%b) : 2

        */
    }

}
