public class if_else_if_Condition {
    public static void main(String[] args) {
        int y =30;
        if(y>50){
            System.out.println("value have if condition block  ");
        }else if (y>40){
            System.out.println("value have else if condition block ");
        }else {
            System.out.println("value dos't match ");
        }
    }
}
